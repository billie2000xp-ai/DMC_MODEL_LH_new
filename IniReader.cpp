getfile::getfile(string filename) {
    std::ifstream pfile;
    std::string line ,valueString;
    unsigned long key = 0;
    size_t commentIndex;
    pfile.open(filename.c_str());
    if (!pfile.is_open()) {
        return;
    }

    while (!pfile.eof()) {
        //getline(pfile,line)
        getline(pfile, line);
        line_message val;
        if (pfile.bad()) {
            std::cout << "cannot read drive file" << endl;
            return;
        }

        if (line.size() == 0) continue;
        if ((commentIndex = line.find_first_of(";")) != string::npos) {
            if (commentIndex == 0) continue;
            line = line.substr(0, commentIndex);
        }

        size_t whiteSpaceEndIndex = line.find_last_not_of(" \t");
        if (whiteSpaceEndIndex != string::npos) {
            line = line.substr(0, whiteSpaceEndIndex + 1);
        }
        size_t inindex;
        if ((inindex = line.find("INCR")) != string::npos) {
            val.issue = START;
            valueString = line.substr(inindex + 5, inindex + 8);
            key = strtoul(valueString.c_str(), NULL, 10);
        } else if ((inindex = line.find("Over")) != string::npos) {
            val.issue = END;
        } else if ((inindex = line.find("Data")) != string::npos) {
            val.issue = DAT;
            valueString = line.substr(inindex + 5, inindex + 8);
            key = strtoul(valueString.c_str(), NULL, 10);
        }

        size_t ns_index = line.find("ns");
        valueString = line.substr(1, ns_index - 1);
        if (val.issue == END) {
            val.task = strtoul(valueString.c_str(), NULL, 10);
        }

        size_t readindex;
        if ((readindex = line.find("Read")) != string::npos) {
            val.operation = "Read";
        } else if ((readindex = line.find("Write")) != string::npos) {
            val.operation = "Write";
        }

        size_t sizdindex;
        if ((sizdindex = line.find("len")) != string::npos) {
            valueString = line.substr(sizdindex + 5, 3);
            val.len = atoi(valueString.c_str());
        }
        size_t index0x;
        if ((index0x = line.find("0x")) != string::npos) {
            valueString = line.substr(index0x + 4,8);
            string line_l = line.substr(index0x,4);
            val.address_l = strtoul(valueString.c_str(),NULL,16);
            val.address_h = strtoul(line_l.c_str(),NULL,16);
        }

        if ((readindex = line.find("id")) != string::npos) {
            valueString = line.substr(readindex + 5, 4);
            val.id = strtoul(valueString.c_str(), NULL, 16);
        }

        if ((index0x = line.rfind("0x")) != string::npos) {
            valueString = line.substr(index0x , 10);
            if (val.issue == START || val.issue == DAT) {
                val.task = strtoul(valueString.c_str(), NULL, 16);
            } else {
                key = strtoul(valueString.c_str(), NULL, 16);
            }
        }

        if (START == val.issue) {
            if (start_valmap.find(key) != start_valmap.end()) {
                key = key + 1;
            }
            start_valmap[key] = val;
        } else if (END == val.issue) {
            if (end_valmap.find(key) != end_valmap.end()) {
                key = key + 1;
            }
            end_valmap[key] = val;
        } else if (DAT == val.issue) {
            if (data_valmap.find(key) != data_valmap.end()) {
                key = key + 1;
            }
            data_valmap[key] = val;
        }
    }
}

