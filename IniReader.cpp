void IniReader::CheckParameter() {
    if (NUM_CHANS == 0) {
        ERROR("Zero channels");
        assert(0);
    }

    if (RDATA_TYPE >= 2) {
        ERROR("RDATA_TYPE error! Currner is "<<RDATA_TYPE<<".");
        assert(0);
    }

    if (GRP_RANK_MODE >= 2) {
        ERROR("GRP_RANK_MODE error! Currner is "<<GRP_RANK_MODE<<".");
        assert(0);
    }

    if (GRP_RANK_PREPB && GRP_RANK_PREAB) {
        ERROR("Set both GRP_RANK_PREPB and GRP_RANK_PREAB true is error.");
        assert(0);
    }

    if (CORE_CONCURR == 0) {
        ERROR("CORE_CONCURR error! Currner is "<<CORE_CONCURR<<".");
        assert(0);
    }

    for (auto map_cfg : MAP_CONFIG) {
        auto first = map_cfg.first;
        auto second = map_cfg.second;
        if (first == "TIMEOUT_PRI_RD" || first == "TIMEOUT_PRI_WR" ||
                first == "ADAPT_PRI_RD" || first == "ADAPT_PRI_WR") {
            if (second.size() != 7) {
                ERROR("Wrong "<<first<<" size: "<<second.size()<<", Must be 7.");
                assert(0);
            }
        }
        if (first == "MPAM_TIMEOUT_RD" || first == "MPAM_TIMEOUT_WR" ||
                first == "MPAM_ADAPT_RD" || first == "MPAM_ADAPT_WR") {
            if (second.size() != 16) {
                ERROR("Wrong "<<first<<" size: "<<second.size()<<", Must be 16.");
                assert(0);
            }
        }
        if (first == "BANK_CMD_TH" || first == "WR_LEVEL0" || first == "WR_LEVEL1" || first == "WR_LEVEL2" ||
                first == "WR_LEVEL3" || first == "WR_MOST_LEVEL0" || first == "WR_MOST_LEVEL1" ||
                first == "WR_MOST_LEVEL2" || first == "WR_MOST_LEVEL3" || first == "RD_LEVEL0" ||
                first == "RD_LEVEL1" || first == "RD_LEVEL2" || first == "RD_LEVEL3" || first == "NO_CMD_SCH_TH" ||
                first == "GRP_MODE_LEVEL0" || first == "GRP_MODE_LEVEL1" || first == "GRP_MODE_LEVEL2" ||
                first == "GRP_MODE_LEVEL3" || first == "WGRP_RANK_MODE") {
            if (second.size() != 5) {
                ERROR("Wrong "<<first<<" size: "<<second.size()<<", Must be 5.");
                assert(0);
            }
        }
        if (first == "ASREF_ADAPT_LEVEL") {
            if (second.size() != 3) {
                ERROR("Wrong "<<first<<" size: "<<second.size()<<", Must be 3.");
                assert(0);
            }
        }
        if (first == "ASREF_ADAPT_PRD") {
            if (second.size() != 4) {
                ERROR("Wrong "<<first<<" size: "<<second.size()<<", Must be 4.");
                assert(0);
            }
        }
        if (first == "ENH_PAGE_ADPT_LVL") {
            if (second.size() != 3) {
                ERROR("Wrong "<<first<<" size: "<<second.size()<<", Must be 3.");
                assert(0);
            }
        }
        if (first == "ENH_PAGE_ADPT_TIME") {
            if (second.size() != 4) {
                ERROR("Wrong "<<first<<" size: "<<second.size()<<", Must be 4.");
                assert(0);
            }
        }
        if (first == "DMC_BW_LEVEL") {
            if (second.size() != 3) {
                ERROR("Wrong "<<first<<" size: "<<second.size()<<", Must be 3.");
                assert(0);
            }
        }
        if (POWER_EN) {
            if (first == "POWER_RD_K" || first == "POWER_WR_K") {
                if (second.size() != 6) {
                    ERROR("Wrong "<<first<<" size: "<<second.size()<<", Must be 6.");
                    assert(0);
                }
            }
            if (first == "POWER_QUEUE_K") {
                if (second.size() != TRANS_QUEUE_DEPTH) {
                    ERROR("Wrong "<<first<<" size: "<<second.size()<<", Must be "
                            <<TRANS_QUEUE_DEPTH<<".");
                    assert(0);
                }
            }
        }
    }

    unsigned dmc_bandwidth = DMC_RATE * DMC_DATA_BUS_BITS / WCK2DFI_RATIO / PAM_RATIO / 16;
    unsigned dram_bandwidth = DMC_RATE * JEDEC_DATA_BUS_BITS / 8;
    if (dmc_bandwidth < dram_bandwidth) {
        ERROR("DMC bandwidth not match DRAM! DMC is "<<dmc_bandwidth<<"MByte, DRAM is "
                <<dram_bandwidth<<"MByte.");
        assert(0);
    }

    if (SBR_WEIGHT_MODE >= 6) { // undefined mode
        ERROR("Unkown SBR_WEIGHT_MODE, mode is "<<SBR_WEIGHT_MODE);
        assert(0);
    }

    if (QOS_POLICY < 1 || QOS_POLICY > 2) { // undefined mode
        ERROR("Unkown QOS_POLICY, mode is "<<QOS_POLICY);
        assert(0);
    }

    if (EM_MODE >=3) { //undefined mode
        ERROR("Unkown EM_MODE, mode is "<<EM_MODE);
        assert(0);
    }
}

